                <p>jqGrid的主要特点为：</p>
                <ol>
                    <li>基于jquery UI主题，开发者可以根据客户要求更换不同的主题</li>
                    <li>兼容目前所有流行的web浏览器</li>
                    <li>Ajax分页，可以控制每页显示的记录数</li>
                    <li>支持XML，JSON，数组形式的数据源</li>
                    <li>提供丰富的选项配置及方法事件接口</li>
                    <li>支持表格排序，支持拖动列、隐藏列</li>
                    <li>支持滚动加载数据</li>
                    <li>支持实时编辑保存数据内容</li>
                    <li>支持子表格及树形表格</li>
                    <li>支持多语言</li>
                    <li>免费</li>
                </ol>
